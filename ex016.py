'''16 - Escreva um programa que pede que o usuário dê entrada em dois valores, em seguida, exiba em 
tela o resultado da soma, subtração, multiplicação e divisão desses números:'''

numero1 = int(input('Insira um número: '))
numero2 = int(input('Insira outro número: '))

